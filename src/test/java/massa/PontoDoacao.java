package massa;

public class PontoDoacao {

	public String nome;
	public String email;
	public String cep;
	public String numero;
	public String complemento;
	public String pets;

	public PontoDoacao(String nome, String email, String cep, String numero, String complemento, String pets) {
		this.nome = nome;
		this.email = email;
		this.cep = cep;
		this.numero = numero;
		this.complemento = complemento;
		this.pets = pets;
	}
}
